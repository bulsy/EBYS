package ebys;

public class Not {
    private int vize;
    private int proje;
    private int finaln;
    
    public Not(){
    	this.vize=0;
    	this.proje=0;
    	this.finaln=0;
    }

	public int getVize() {
		return vize;
	}

	public void setVize(int vize) {
		this.vize = vize;
	}

	public int getProje() {
		return proje;
	}

	public void setProje(int proje) {
		this.proje = proje;
	}

	public int getFinaln() {
		return finaln;
	}

	public void setFinaln(int finaln) {
		this.finaln = finaln;
	}
	
	
}
