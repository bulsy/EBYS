public class Yuzde {
    private double vize;
    private  double proje;
    private double finaln;

    public Yuzde(){
        this.vize=0;
        this.proje=0;
        this.finaln=0;
    }

    public double getProje() {
        return proje;
    }

    public void setFinaln(double finaln) {
        this.finaln = finaln;
    }

    public void setProje(double proje) {
        this.proje = proje;
    }

    public void setVize(double vize) {
        this.vize = vize;
    }

    public double getVize() {
        return vize;
    }

    public double getFinaln() {
        return finaln;
    }
}
